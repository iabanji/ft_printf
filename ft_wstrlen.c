#include "libft.h"

size_t	ft_wstrlen(wchar_t *str)
{
	size_t	n;
	size_t	i;

	n = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] <= 127)
			n++;
		else if (str[i] <= 2047)
			n += 2;
		else if (str[i] <= 65535)
			n += 3;
		else if (str[i] <= 1114111)
			n += 4;
		i++;
	}
	return (n);
}
